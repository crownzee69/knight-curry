// Helper function to get item by ID
export function getMenuItemById(id: string): MenuItemDetail | undefined {
  return menuItemDetails.find(item => item.id === id);
}

// Helper function to get items by category
export function getMenuItemsByCategory(categoryId: string): MenuItemDetail[] {
  return menuItemDetails.filter(item => item.categoryId === categoryId);
}

// Helper function to get all late night special items
export function getLateNightSpecials(): MenuItemDetail[] {
  return menuItemDetails.filter(item => item.isLateNightSpecial === true);
}

// Helper function to get all unique categories
export function getAllCategories(): { id: string; name: string; categoryId: string }[] {
  const categoryMap = new Map<string, { id: string; name: string; categoryId: string }>();
  
  menuItemDetails.forEach(item => {
    if (!categoryMap.has(item.categoryId)) {
      categoryMap.set(item.categoryId, {
        id: item.categoryId,
        name: item.category,
        categoryId: item.categoryId,
      });
    }
  });
  
  return Array.from(categoryMap.values());
}
